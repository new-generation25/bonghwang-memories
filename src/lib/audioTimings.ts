/**
 * 이 큐에 쓸 수 있는 실측 시각을 돌려준다.
 *
 * 줄 수가 맞지 않으면(대사가 수정됐는데 시각을 다시 재지 않은 경우)
 * null을 돌려 추정으로 되돌아간다 — 어긋난 값으로 자막이 튀는 것보다
 * 대략 맞는 추정이 낫다.
 */
export function timingsFor(audioFile: string, lineCount: number): number[] | null {
  const t = AUDIO_TIMINGS[audioFile]
  if (!t || t.length !== lineCount) return null
  // 오름차순이 아니면 잘못 잰 것이다
  for (let i = 1; i < t.length; i++) if (t[i] < t[i - 1]) return null
  return t
}
